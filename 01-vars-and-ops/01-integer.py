# Example of integer numbers
# + - * -> operators
# / % // -> operators
print("Start")

number_one = 1
number_two = 2
number_five = 5

print("****")
print(number_one)
print(number_two)
print(number_five)
print("****")

number_three = number_one + number_two
number_four = number_two + number_two
number_ten = number_two * number_five
number_eight = number_ten - number_five
number_seven = number_four + number_three

div_two = number_five / number_two
print("5 / 2")
print(div_two)
print("****")

div_two_integer = number_five // number_two
print("5 // 2")
print(div_two_integer)

print("****")
mod_three = number_seven % number_four
print("7 % 4")
print(mod_three)
